#!/usr/bin/env node

/**
 * generate-safelist.mjs
 *
 * Generates common/safelist.css from the TrueColor type defined in
 * common/theme/Theme.ts.  Because Theme.ts is TypeScript (ESM) and we
 * need a Node.js script, TrueColor values are hardcoded here to mirror
 * the union in Theme.ts.
 *
 * Output format:
 *   - @source inline() with brace expansion for simple patterns
 *     (no opacity or pseudo-selector modifiers).
 *   - @layer utilities with raw CSS for non-standard opacity values
 *     (glass fills /55,/65,/75,/85) and complex patterns with
 *     pseudo-selectors.
 */

// ── TrueColor values (must match common/theme/Theme.ts) ──────────
const COLORS = [
  "red","orange","amber","yellow","lime","green","emerald","teal",
  "cyan","sky","blue","indigo","violet","purple","fuchsia","rose",
  "slate","gray","zinc","neutral","stone",
];

const COLOR_LIST = COLORS.join(",");

// ── Helpers ───────────────────────────────────────────────────────
function brace(pattern) {
  return pattern.replace(/\{COLOR\}/g, `{${COLOR_LIST}}`);
}

// ── Output buffer ─────────────────────────────────────────────────
const lines = [];
const L = (...args) => lines.push(args.join(""));

// Header
L(`/* Tailwind v4 safelist — dynamically generated from TrueColor */`);
L(`/* Generated by scripts/generate-safelist.mjs — do not edit manually */`);
L("");

// ═══════════════════════════════════════════════════════════════════
// SECTION 1 — @source inline() with brace expansion (simple classes)
// ═══════════════════════════════════════════════════════════════════
L(`/* ── Simple patterns via @source inline() + brace expansion ──── */`);
L("");

function src(pattern) {
  L(brace(pattern));
}

// bg-{color}-{shade}
src(`@source inline("bg-{COLOR}-50");`);
src(`@source inline("bg-{COLOR}-100");`);
src(`@source inline("bg-{COLOR}-200");`);
src(`@source inline("bg-{COLOR}-300");`);
src(`@source inline("bg-{COLOR}-400");`);
src(`@source inline("bg-{COLOR}-500");`);
src(`@source inline("bg-{COLOR}-600");`);
src(`@source inline("bg-{COLOR}-700");`);
src(`@source inline("bg-{COLOR}-900");`);
L("");

// text-{color}-{shade}
src(`@source inline("text-{COLOR}-50");`);
src(`@source inline("text-{COLOR}-100");`);
src(`@source inline("text-{COLOR}-200");`);
src(`@source inline("text-{COLOR}-300");`);
src(`@source inline("text-{COLOR}-400");`);
src(`@source inline("text-{COLOR}-500");`);
src(`@source inline("text-{COLOR}-600");`);
src(`@source inline("text-{COLOR}-700");`);
src(`@source inline("text-{COLOR}-900");`);
L("");

// border-{color}-{shade}
src(`@source inline("border-{COLOR}-200");`);
src(`@source inline("border-{COLOR}-300");`);
src(`@source inline("border-{COLOR}-500");`);
L("");

// hover:bg-{color}-{shade}
src(`@source inline("hover:bg-{COLOR}-100");`);
src(`@source inline("hover:bg-{COLOR}-400");`);
L("");

// hover:text-{color}-{shade}
src(`@source inline("hover:text-{COLOR}-500");`);
L("");

// hover:border-{color}-{shade}
src(`@source inline("hover:border-{COLOR}-200");`);
L("");

// dark:bg-{color}-{shade}
src(`@source inline("dark:bg-{COLOR}-300");`);
src(`@source inline("dark:bg-{COLOR}-400");`);
src(`@source inline("dark:bg-{COLOR}-500");`);
src(`@source inline("dark:bg-{COLOR}-600");`);
src(`@source inline("dark:bg-{COLOR}-700");`);
src(`@source inline("dark:bg-{COLOR}-900");`);
L("");

// dark:text-{color}-{shade}
src(`@source inline("dark:text-{COLOR}-50");`);
src(`@source inline("dark:text-{COLOR}-100");`);
src(`@source inline("dark:text-{COLOR}-200");`);
src(`@source inline("dark:text-{COLOR}-300");`);
src(`@source inline("dark:text-{COLOR}-400");`);
src(`@source inline("dark:text-{COLOR}-500");`);
src(`@source inline("dark:text-{COLOR}-600");`);
src(`@source inline("dark:text-{COLOR}-700");`);
src(`@source inline("dark:text-{COLOR}-900");`);
L("");

// dark:border-{color}-{shade}
src(`@source inline("dark:border-{COLOR}-300");`);
src(`@source inline("dark:border-{COLOR}-500");`);
src(`@source inline("dark:border-{COLOR}-700");`);
L("");

// focus-visible:ring-{color}-{shade}
src(`@source inline("focus-visible:ring-{COLOR}-400");`);
src(`@source inline("focus-visible:ring-{COLOR}-500");`);
L("");

// focus-visible:ring-offset-2
L(`@source inline("focus-visible:ring-offset-2");`);
L("");

// after:bg-{color}-{shade}
src(`@source inline("after:bg-{COLOR}-500");`);
L("");

// dark:hover:bg-{color}-{shade}
src(`@source inline("dark:hover:bg-{COLOR}-300");`);
src(`@source inline("dark:hover:bg-{COLOR}-500");`);
L("");

// dark:hover:text-{color}-{shade}
src(`@source inline("dark:hover:text-{COLOR}-100");`);
src(`@source inline("dark:hover:text-{COLOR}-200");`);
src(`@source inline("dark:hover:text-{COLOR}-300");`);
L("");

// dark:hover:border-{color}-{shade}
src(`@source inline("dark:hover:border-{COLOR}-200");`);
src(`@source inline("dark:hover:border-{COLOR}-300");`);
L("");

// dark:after:bg-{color}-{shade}
src(`@source inline("dark:after:bg-{COLOR}-400");`);
L("");

// peer-checked:bg-{color}-{shade}
src(`@source inline("peer-checked:bg-{COLOR}-500");`);
L("");

// peer-checked:border-{color}-{shade}
src(`@source inline("peer-checked:border-{COLOR}-500");`);
L("");

// peer-focus:ring-{color}-{shade}
src(`@source inline("peer-focus:ring-{COLOR}-400");`);
L("");

// ── Padding utilities (no color dependency) ───────────────────────
L(`/* ── Padding utilities ─────────────────────────────────────── */`);
L(`@source inline("p-0.5");`);
L(`@source inline("p-1");`);
L(`@source inline("p-1.5");`);
L(`@source inline("p-2");`);
L(`@source inline("p-3");`);
L("");

// accent-{color}-{shade}
src(`@source inline("accent-{COLOR}-600");`);
L("");

// dark:peer-checked:bg-{color}-{shade}
src(`@source inline("dark:peer-checked:bg-{COLOR}-400");`);
L("");

// dark:focus-visible:ring-{color}-{shade}
src(`@source inline("dark:focus-visible:ring-{COLOR}-400");`);
L("");

// ═══════════════════════════════════════════════════════════════════
// SECTION 2 — @layer utilities (non-standard opacity + complex)
// ═══════════════════════════════════════════════════════════════════
L(`/* ── Non-standard opacity / complex patterns via @layer utilities ─ */`);
L("");
L(`@layer utilities {`);
L("");

// ── Glass fill classes (arbitrary opacity values) ─────────────────
L(`  /* ── Light: bg-{color}-100/{opacity} ─────────────────────── */`);
const glassOpsLight = [55, 65, 75, 85];
for (const color of COLORS) {
  for (const op of glassOpsLight) {
    L(`  .bg-${color}-100\\/${op}  { background-color: color-mix(in srgb, var(--color-${color}-100) ${op}%, transparent) !important; }`);
  }
  L("");
}

L(`  /* ── Dark: dark:bg-{color}-600/{opacity} ─────────────────── */`);
const glassOpsDark = [5, 10, 15, 25, 35];
for (const color of COLORS) {
  for (const op of glassOpsDark) {
    L(`  .dark\\\:bg-${color}-600\\/${op}  { background-color: color-mix(in srgb, var(--color-${color}-600) ${op}%, transparent) !important; }`);
  }
  L("");
}

L(`  /* ── Hover: hover:bg-{color}-100/{opacity+10} ────────────── */`);
for (const color of COLORS) {
  for (const op of glassOpsLight) {
    L(`  .hover\\\:bg-${color}-100\\/${op + 10}  { background-color: color-mix(in srgb, var(--color-${color}-100) ${op + 10}%, transparent) !important; }`);
  }
  L("");
}

L(`  /* ── Dark hover: dark:hover:bg-{color}-600/{opacity+10} ──── */`);
for (const color of COLORS) {
  for (const op of glassOpsDark) {
    L(`  .dark\\\:hover\\\:bg-${color}-600\\/${op + 10}  { background-color: color-mix(in srgb, var(--color-${color}-600) ${op + 10}%, transparent) !important; }`);
  }
  L("");
}

// ── Focus ring with arbitrary opacity /50 ─────────────────────────
L(`  /* ── Peer focus ring /50 ─────────────────────────────────── */`);
for (const color of COLORS) {
  L(`  .peer-focus\:ring-${color}-400\\/50  { --tw-ring-color: color-mix(in srgb, var(--color-${color}-400) 50%, transparent) !important; }`);
}
L("");

// ── Spinner border utilities ──────────────────────────────────────
L(`  /* ── Spinner: border-t-{color}-500 ───────────────────────── */`);
for (const color of COLORS) {
  L(`  .border-t-${color}-500  { border-top-color: var(--color-${color}-500); }`);
}
L("");

L(`  /* ── Spinner: dark:border-t-{color}-300 ──────────────────── */`);
for (const color of COLORS) {
  L(`  .dark\\\:border-t-${color}-300  { border-top-color: var(--color-${color}-300); }`);
}
L("");

L(`  /* ── Spinner: border-r-{color}-300 ───────────────────────── */`);
for (const color of COLORS) {
  L(`  .border-r-${color}-300  { border-right-color: var(--color-${color}-300); }`);
}
L("");

L(`  /* ── Spinner: dark:border-r-{color}-200 ──────────────────── */`);
for (const color of COLORS) {
  L(`  .dark\\\:border-r-${color}-200  { border-right-color: var(--color-${color}-200); }`);
}
L("");

L(`  /* ── Spinner: border-b-{color}-200 ───────────────────────── */`);
for (const color of COLORS) {
  L(`  .border-b-${color}-200  { border-bottom-color: var(--color-${color}-200); }`);
}
L("");

L(`  /* ── Spinner: dark:border-b-{color}-100/60 ───────────────── */`);
for (const color of COLORS) {
  L(`  .dark\\\:border-b-${color}-100\\/60  { border-bottom-color: color-mix(in srgb, var(--color-${color}-100) 60%, transparent); }`);
}
L("");

L(`  /* ── Spinner: border-l-{color}-100 ───────────────────────── */`);
for (const color of COLORS) {
  L(`  .border-l-${color}-100  { border-left-color: var(--color-${color}-100); }`);
}
L("");

L(`  /* ── Spinner: dark:border-l-{color}-100/40 ───────────────── */`);
for (const color of COLORS) {
  L(`  .dark\\\:border-l-${color}-100\\/40  { border-left-color: color-mix(in srgb, var(--color-${color}-100) 40%, transparent); }`);
}
L("");

L(`} /* end @layer utilities */`);

// ── Write output ──────────────────────────────────────────────────
import { writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const __dirname = dirname(fileURLToPath(import.meta.url));
const OUTPUT_PATH = join(__dirname, "..", "common", "safelist.css");

writeFileSync(OUTPUT_PATH, lines.join("\n") + "\n", "utf-8");
console.log(`Wrote ${OUTPUT_PATH} (${lines.length} lines)`);